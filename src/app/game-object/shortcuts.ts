/** Special system commands as defined in the defaults configuration. */
export enum systemShortcuts {
  /** Show the current time. */
  Clock = 'C',

  /** Drop an entity from the inventoriy. */
  Drop = '!',

  /** Show the current surrounding. */
  Look = 'L',

  /** Pick an entity and add it to the inventory. */
  Pick = '<',

  /** Leave the game and give up. */
  Quit = 'Q',

  /** Say something. */
  Say = '&',

  /** Show the current surrounding and the inventory. */
  View = '?',
}
